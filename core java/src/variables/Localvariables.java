package variables;

public class Localvariables {
	public static void a1()
	{
		//Local variables
		int a=10; int b=20;
		System.out.println(a*b);
		
	}
	public static void a2()
	{
		//Local variables
		int a=5; int b=8;
		System.out.println(a*b);
	}

	public static void main(String[] args) {
		a1();
		a2();
		}
	}
